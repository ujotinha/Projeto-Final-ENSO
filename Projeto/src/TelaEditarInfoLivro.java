    public void InfoLivro(int id) {
        titulo = livro.obterNomeLivro(id);
        autor = livro.obterAutorLivro(id);
        genero = livro.obterGeneroLivro(id);
        sinopse = livro.obterSinopseLivro(id);
        id_livro = id;

        Titulo.setText(titulo);
        Autor.setText(autor);
        Genero.setText(genero);
        Sinopse.setText(sinopse);
        NotaLivro.setSelectedIndex(livro.obterNotaLivro(id));
    }


