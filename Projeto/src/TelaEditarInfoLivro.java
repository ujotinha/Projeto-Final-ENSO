    private void BotEditarLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotEditarLivroActionPerformed
        // TODO add your handling code here:
        if (Titulo.getText() != null && Autor.getText() != null && Genero.getText() != null && Sinopse.getText() != null) {
            String nome_antigo;
            nome_antigo = livro.obterNomeLivro(id_livro);
            livro.atualizarInfo(id_livro, Titulo.getText(), Autor.getText(), Genero.getText(), Sinopse.getText(), NotaLivro.getSelectedIndex());
            capa.renomearArquivo("C:\\Users\\João\\Documents\\NetBeansProjects\\Telas\\src\\CapaLivros\\" + nome_antigo + ".jpg", Titulo.getText() + ".jpg");
            if (Caminho.getText() != null) {
                capa.CopiarImagem(Caminho.getText(), Titulo.getText());
            }
            JOptionPane.showMessageDialog(null, "Edição bem-sucedida");
            new TelaGerCatalogo().setVisible(true);
            setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "Você não pode deixar nenhum campo vazio, exceto o Caminho do arquivo.");
        }
    }//GEN-LAST:event_BotEditarLivroActionPerformed

