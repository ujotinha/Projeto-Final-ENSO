    private void BotExibirSenhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotExibirSenhaActionPerformed
        // TODO add your handling code here:
        if (i%2==0){
            OlhoFechado.setVisible(false);
            OlhoAberto.setVisible(true);
            Senha.setEchoChar((char)0);
        } else {
            OlhoFechado.setVisible(true);
            OlhoAberto.setVisible(false);
            Senha.setEchoChar('*');
        }
        i++;
    }//GEN-LAST:event_BotExibirSenhaActionPerformed

