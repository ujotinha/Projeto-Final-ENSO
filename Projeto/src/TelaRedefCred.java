    private void BotRedefinirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotRedefinirActionPerformed
        // TODO add your handling code here:
        int id;
        boolean is_adm;
        id = credenciais.obterIDAtual();
        is_adm = credenciais.obterIs_AdmAtual();
        String senha = new String (Senha.getPassword());
        credenciais.removerCredAntiga(id);
        credenciais.inserirUsuario(id, Nome.getText(), senha, Email.getText(), is_adm);
        credenciais.AtualizarUsuarioAtual(id, Nome.getText(), Email.getText(), senha, is_adm);
        new TelaRedefinicaoSucesso().setVisible(true);
        setVisible(false);
        
    }//GEN-LAST:event_BotRedefinirActionPerformed

