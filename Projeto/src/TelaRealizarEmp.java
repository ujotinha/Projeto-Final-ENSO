    private void BotEmprestimoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotEmprestimoActionPerformed
        // TODO add your handling code here:
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        String dataTexto = Dt_Dev.getText().trim();

        if (dataTexto.isEmpty() || dataTexto.contains("_")) {
            JOptionPane.showMessageDialog(this, "Por favor, insira uma data de devolução válida.");
            return;
        }
        LocalDate Dt_Devolucao = LocalDate.parse(dataTexto, formato);
        LocalDate Dt_Emprestimo = LocalDate.now();

        String retorno = livro.inserirEmprestimo(id, livro.obterIDAtual(), Dt_Emprestimo, Dt_Devolucao);
        if (retorno.equalsIgnoreCase("Emprestimo realizado com sucesso!")) {
            new TelaEmpSucesso().setVisible(true);
            setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, retorno);
        }
    }//GEN-LAST:event_BotEmprestimoActionPerformed

