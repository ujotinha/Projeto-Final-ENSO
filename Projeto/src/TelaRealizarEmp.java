    private void mostrarInfo(int id) {
        titulo = livro.obterNomeLivro(id);
        autor = livro.obterAutorLivro(id);
        genero = livro.obterGeneroLivro(id);
        responsavel = livro.obterNomeAtual();
        LocalDate dataAtual = LocalDate.now();
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        email = livro.obterEmailAtual();

        Titulo.setText(titulo);
        Autor.setText(autor);
        Genero.setText(genero);
        Responsavel.setText(responsavel);
        Dt_Emp.setText(dataAtual.format(formato));
        Email.setText(email);

    }
