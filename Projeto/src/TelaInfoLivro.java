    public void verInfo(int id){
        titulo = livro.obterNomeLivro(id);
        autor = livro.obterAutorLivro(id);
        genero = livro.obterGeneroLivro(id);
        sinopse = livro.obterSinopseLivro(id);
        id_livro = id;
        
        NomeLivro.setText(titulo);
        NomeAutor.setText(autor);
        GeneroLivro.setText(genero);
        SinopseLivro.setText(sinopse);
        ID.setText("ID: "+ id_livro);
        Nota.setText(""+ livro.obterNotaLivro(id));
    }
